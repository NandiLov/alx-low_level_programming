/**
 * largest_number - returns the largest of 3 numbers
 * @int a: first integer
 * @int b: second integer
 * @int c: third integer
 * Return: largest number
*/
largest_number(int)
{
	int largest_number(int a, int b, int c)

	int largest = a;

	if (b > largest)
	{
	largest = b;
	}
	if (c > largest)
	{
	largest = c;
	}

	return (largest);
}
